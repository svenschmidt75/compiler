void SSLEXScanner::txtFilter( char *from, char *to, ulong cnt )
{
	//copy cnt bytes from from to to, but transform escape sequences into
	//readable text form
	ulong	i;
	char	c, tmp[10];

	//init
	*to = 0;

	//as long as there is another char
	for( i = 0; i < cnt; i++ )
	{
		//get char
		c = *from++;

		//is it printable?
		if( !isprint( c ) )
		{
			//transform into readable
			switch( c )
			{
				//newline?
				case '\n':
				{
					//newline
					strcat( to, "\\n" );
					break;
				}

				//Backspace
				case '\b':
				{
					//Backspace
					strcat( to, "\\b" );
					break;
				}

				//Linefeed
				case '\f':
				{
					//Linefeed
					strcat( to, "\\f" );
					break;
				}

				//carriage return
				case '\r':
				{
					//carriage return
					strcat( to, "\\r" );
					break;
				}

				//Tabulator
				case '\t':
				{
					//Tabulator
					strcat( to, "\\t" );
					break;
				}

#if __STDC__
				//bing
				case '\a':
				{
					//bing
					strcat( to, "\\a" );
					break;
				}

				//vertical tab
				case '\v':
				{
					//vertical tab
					strcat( to, "\\v" );
					break;
				}
#else
				//bing
				case '\a':
				{
					//bing
					strcat( to, "\\007" );
					break;
				}

				//vertical tab
				case '\v':
				{
					//vertical tab
					strcat( to, "\\013" );
					break;
				}
#endif

				default:
				{
					//neither of the above
					sprintf( tmp, "%x", c );

					//append
					strcat( to, tmp );
				}
			}
		}

		else
		{
			//yes, printable
			sprintf( tmp, "%c", c );

			//append
			strcat( to, tmp );
		}
	}
}
